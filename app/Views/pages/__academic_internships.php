                                <h5>Academic Internships</h5>

                                <p>The academic internship program at CHARUSAT is an experiential education opportunity
                                    that provides a link between the classroom and the workplace. It is an opportunity
                                    for the student to learn about her/his area of academic interest through practical
                                    experience.
                                </p>

                                <p>In today's competitive job market, employers are looking for experience coupled with
                                    a college education. Internships allow the students to gain work experience through
                                    an academic relationship with the college and the employer. An internship can be a
                                    single semester or finite time period that may be paid or unpaid, and can be for
                                    academic credit.
                                </p>

                                <p>An internship is required of some majors and can be available for all majors.</p>



