                        <h4>HERBAL GARDEN</h4>
                        <p>
                            Nowadays peoples are moving towards natural remedies to treat
                            their illness or disease. Herbs play a vital role in day to day
                            life of an individual which is benefited for physical and mental
                            health of a person.<br /><br />
                            Ramanbhai Patel College of Pharmacy (RPCP) and Charotar University
                            of Science and Technology (CHARUSAT) has its own Herbal Garden.
                            Herbal garden is situated behind RPCP building and is spread over
                            around 2000 sq meter of land.<br /><br />
                            It has rich source of more than 100 medicinal and aromatic plant
                            species. Some special plants like Rudraksha, coffee, chandan,
                            Costus speciosus, Bixa orellana etc. are also available and
                            maintained in herbal garden.<br /><br />
                            During academics, in practical session students were taken to
                            garden to study the habitate and morphology of plant. Surrounding
                            the herbal garden and in campus also, different seasonal plants
                            are grown. The students learnt the medicinal plant though some are
                            not mentioned in syllabus. In this concrete era at least they can
                            have knowledge of use and importance of different medicinal
                            plant.<br /><br />
                        </p>
