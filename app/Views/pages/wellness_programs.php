                                <h5>PREAMBLE</h5>

                                <p>Students arriving at University for the first time or embarking on a programme may
                                    have to learn to adapt to significant changes, such as moving to a new area,
                                    separation from family and friends, establishing a new social network, managing a
                                    tight budget, combining academic study with family commitments and also adjusting to
                                    different cultures. These changes are exciting, challenging and an intrinsic part of
                                    the attraction of going to University. All these warrant facilitation of students
                                    towards a new turn in their journey of life.
                                </p>

                                <p>CHARUSAT, through Student Counselling System, aims to foster academic excellence,
                                    positive mental attitude and general well-being of the students.

                                    The Student Counselling System is a proactive and potential building activity that
                                    identifies the true potential of the students and brings it out, so as to facilitate
                                    students to achieve their academic and personal goals. The system shall be
                                    comprising of Counsellors, primarily the teachers, and the students themselves.
                                </p>


                                <h5>OBJECTIVES</h5>
                                <p>CHARUSAT, through student counselling system aims to foster:</p>
                                <ul>
                                    <li>Academic Excellence.</li>
                                    <li>Expert Counselling.</li>
                                    <li>General Well-being of the Students.</li>
                                </ul>

                                <h5>THE AREAS LOOKED INTO</h5>
                                <p>The students are being counseled in the areas of:</p>
                                <ul>
                                    <li>Academic Matters: </li>
                                    <li>Social Matters:</li>
                                    <li> Personal Matters affecting his/her career life</li>

                                </ul>

