                                <h5>GENERAL GUIDELINES:</h5>
                                <p>A broad-based ‘Student Counselling System’ is being envisaged which shall be,
                                    interactive and result-oriented, involving students, teachers and parents,
                                    addressing common student concerns ranging from anxiety, stress, fear of change and
                                    failure, homesickness and a slew of academic worries.</p>

                                <p>In view to meet the above:</p>
                                <ul>
                                    <li>
                                        All the Principals/ HoDs (Engg.) shall have to allocate counsellor teachers to
                                        the students at the beginning of the new Term. This has to be widely notified to
                                        the students and parents through display on the notice boards, emails, etc.
                                        within a week of the beginning of the Term.
                                    </li>
                                    <li>Each teacher will act as a Counsellor with a batch ranging from 15 to 25
                                        students allotted to him/her.</li>
                                    <li>
                                        Students allocated to a particular counsellor will remain under his/her guidance
                                        till the completion of their studies (UG/PG Programmes). In the event of Teacher
                                        Counsellor leaving the University, other Teacher Counsellor may be allocated to
                                        the students by Principal immediately.
                                    </li>
