                                    <li>The Teacher Counsellor will need to have a thorough understanding of the credit system/ evaluation system for counselling a student for academic as well as other matters like:</li>
                                    <li>The Counselling should be undertaken in a planned manner to ensure continuity and consistency.</li>
                                    <li>The counsellors shall keep the parents of their counsellees informed regularly at a maximum interval of one month.</li>
                                    <li>The counsellors shall report the progress of their counsellees to the Department Counselling Coordinator every month.</li>
                                    <li>The counselling activities shall be part of Performance Appraisals of the teachers.</li>
                                    <li>The Principals shall have to regularly monitor the activities of Student Counselling and report to Academic Counsel in its meetings.</li>
                                </ul>
